package com.example.novi.cb.model;

public class Sensor {

    private String kode;
    private String value;

    public Sensor() {
    }

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
